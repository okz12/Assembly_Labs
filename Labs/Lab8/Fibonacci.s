	AREA	ArmSubrEx, CODE, READWRITE
	ENTRY
		MOV R0, #0
		MOV R1, #1
		MOV R3, #3
		LDR R13, =STACKL
		CMP R3, #1
		MOVEQ	R2, #1
		MOVMI	R2, #0
		BLE	STOP
		BL	FIB
STOP	B	STOP

FIB		STMEA R13!, {R0, R1, R3, R14}

LOOP
		ADD R2, R1, R0
		MOV R0, R1
		MOV R1, R2
		SUB R3, R3, #1
		CMP R3, #1
		BNE LOOP
		LDMEA R13!, {R0, R1, R3, PC}

STACKL	FILL 40
	END